public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("These are double quotes \" \" "); //Escape Sequence for double quote is \"
        System.out.println("This is a tab space \t !"); //Escape Sequence for tab space is \t
        System.out.println("This is a backslash \\"); //Escape Sequence for backslash is \\
        System.out.println("This \n is \n an \n example \n of \n linebreak!"); //Escape Sequence for linebreak is \n
    }
    
}
