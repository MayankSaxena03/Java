public class Exponents {
    public static void main(String[] args) {
        //You can use Math functions to perform different mathematical operations in java

        //Calculating power
        int a = 5;
        int b = 2;
        System.out.println(Math.pow(a, b));

        //Finding maximum and minimum
        System.out.println("Maximum of a and b is : "+Math.max(a, b));
        System.out.println("Minimum of a and b is : "+Math.min(a, b));

        //Calcuating square root
        int k = 9;
        System.out.println(Math.sqrt(k));
        
        //You can perform more mathematical operations using Math class
    }
}
